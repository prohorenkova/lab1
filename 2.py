a = int(input("Введите номер вашего места в плацкартном вагоне: "))
if a % 2 == 0 and a <= 36:
    print("место ", a, " - Верхнее в купе")
elif a % 2 != 0 and a <= 35:
    print("место ", a, " - Нижнее в купе")
elif a % 2 == 0 and a > 36:
    print("место ", a, " - Верхнее боковое")
else:
    print("место ", a, " - Нижнее боковое")
